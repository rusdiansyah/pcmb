<center><h3>PENERIMAAN MAHASISWA BARU</h3></center>
<p style="text-align:justify">
Satu kenyataan objektif, bahawa sebagian besar dari masyarakat Indonesia menuntut pelaksanaan pengembangan ilmu pengetahuan agama, ilmu pengetahuan dan teknologi dan/atau seni yang dijiwai nilai-nilai keislaman. Tuntutan tersebut tidak bisa dihindarkan dalam rangka mencerdaskan kehidupan bangsa, maka kehadiran Institut Agama Islam Negeri yang mengembangkan ilmu-ilmu keislaman secara terpadu dengan ilmu-ilmu modern Khususnya di Provinsi Banten merupakan keniscayaan.</p>
<br />
<center><h3>Visi</h3></center>
<p style="text-align:justify">
Terwujudnya lembaga pendidikan tinggi agama islam terkemuka di Indonesia yang mampu mengembangkan dan mengintegrasikan aspek keislaman, keilmuan, kemanusiaan, dan keindonesiaan.
</p>
<center><h3>Misi</h3></center>
<p style="text-align:justify">
<ol>
<li>Menyelenggarakan Tri Darma Perguruan Tinggi yang Islami dan berkualitas;</li>
<li>Mewujudkan insan akademis yang cerdas dan berakhlak mulia;</li>
<li>Menumbuhkambangkan etos ilmu, etos kerja dan etos pengabdian yang tinggi, serta berpartisipasi aktif dalam memberdayakan segenap potensi masyarakat.</li>
</ol>
<center><h3>Tujuan</h3></center>
<p style="text-align:justify">
<ol>
<li>Menyediakan lulusan yang memiliki kemampuan dalam bidang kependidikan, kebahasaaan, kesejahteraan, dan kesusastraan;</li>
<li>Menyiapkan lulusan yang memiliki kompetensi pedagogik, profesional, keperibadian dan sosial yang dilandasi oleh nilai-nilai ahlakul karimah.</li>
</ol>
<br />
